/**
 * Voluntario
 */
public class Voluntario {

    private int idVoluntario;
    private String nomeVoluntario;
    private int idadeVoluntario;
    private String cpfVoluntario;
    private String enderecoVoluntario;
    private String telefone;
    public int getIdVoluntario() {
        return idVoluntario;
    }
    public void setIdVoluntario(int idVoluntario) {
        this.idVoluntario = idVoluntario;
    }
    public String getNomeVoluntario() {
        return nomeVoluntario;
    }
    public void setNomeVoluntario(String nomeVoluntario) {
        this.nomeVoluntario = nomeVoluntario;
    }
    public int getIdadeVoluntario() {
        return idadeVoluntario;
    }
    public void setIdadeVoluntario(int idadeVoluntario) {
        this.idadeVoluntario = idadeVoluntario;
    }
    public String getCpfVoluntario() {
        return cpfVoluntario;
    }
    public void setCpfVoluntario(String cpfVoluntario) {
        this.cpfVoluntario = cpfVoluntario;
    }
    public String getEnderecoVoluntario() {
        return enderecoVoluntario;
    }
    public void setEnderecoVoluntario(String enderecoVoluntario) {
        this.enderecoVoluntario = enderecoVoluntario;
    }
    public String getTelefone() {
        return telefone;
    }
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }
    
}
